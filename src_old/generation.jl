using LinearAlgebra
using AlgebraicNumbers: AlgebraicNumber

function Base.isless(a::AlgebraicNumber, b::AlgebraicNumber)
    return ((-a.coeff[1]) < (-b.coeff[1]))
end

function Base.AbstractFloat(a::AlgebraicNumber)
    return (Float64(-a.coeff[1]))
end

"""
    find_minimum_dist(coordinates, minimums=1)

Find minimum distances in a vector of coordinates up to the number of minimums
specified. Takes in coordinates of any dimension

```julia-repl
julia> find_minimum_dist([0, 0], [[0, 0], [0, 1], [1, 0]], 2)
2-element Vector{Float64}:
 0.0
 1.0
"""
function find_minimum_dist(
    center::Vector{T},
    coordinates::Vector{Vector{T}},
    minimums = 1,
) where {T<:Union{Real,Int,Float64,AlgebraicNumber}}
    relative_distances::Vector{AlgebraicNumber} =
        collect(Iterators.map(coord -> sum((coord - center) .^ 2), coordinates))
    unique!(relative_distances)
    sort!(relative_distances)
    relative_distances[1:minimums]
end

"""
    generate_coordinates(basis, bravais_lattice, max_order)

Find every coordinate generated by the bravais lattice up till maximum_order,
then repeat for every element of the basis.

```julia-repl
julia> generate_coordinates([[0, 0]], [[0, 1], [1, 0]], 3)
"""
function generate_coordinates(
    basis::Vector{Vector{T}},
    bravais_lattice::Vector{Vector{T}},
    max_order::Int64,
) where {T<:Union{Real,Int,Float64,AlgebraicNumber}}
    dimensions = size(bravais_lattice)[1]
    coordinates::Vector{Vector{T}} = []
    # Cover each basis
    for basis_elem in basis
        # Generate all integers for coordinates in that basis
        for int_coord = 0:((max_order^dimensions)-1)
            coordinate = []
            int_coord_temp = divrem(int_coord, max_order)
            # Find each coordinate 
            for dimension = 1:dimensions
                pushfirst!(coordinate, int_coord_temp[2])
                int_coord_temp = divrem(int_coord_temp[1], max_order)
            end
            # Shift according to the bravais lattice and basis
            push!(coordinates, sum(coordinate .* bravais_lattice) .+ basis_elem)
        end
    end
    coordinates
end

