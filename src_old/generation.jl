function generate_lattice(
    basis::Vector{Vector{T}},
    bravais_lattice::Vector{Vector{T}},
    neighborhood::Int,
    max_order::Int,
) where {T<:Union{Real,Int,Float64,AlgebraicNumber}}

    generated_lattice = MetaGraph(
        DiGraph();
        label_type = Int,
        vertex_data_type = Vector{T},
        edge_data_type = NTuple{2,Int},
        graph_data = "Cluster Generated by bravais lattice $bravais_lattice and basis $basis, up to maximum order $max_order, with $neighborhood nearest neighbors",
    )

    # First populate the graph with all possible vertices
    full_lattice = generate_coordinates(basis, bravais_lattice, max_order)
    for (index_coord, coord) in enumerate(full_lattice)
        generated_lattice[index_coord] = coord
    end

    # Find the center and possible distances, directions to be populated later
    center = sum(repeat([div(max_order, 2)], size(bravais_lattice)[1]) .* bravais_lattice)
    distances = find_minimum_dist(center, full_lattice, neighborhood + 1)
    directions::Vector{Vector{T}} = []

    for (index_coord, coord) in enumerate(full_lattice)
        for (index_distance, distance) in enumerate(distances[2:end])
            # This looks weird, but its because the distance array is indexed again from the 
            # second element in the enumerate function
            within_distance =
                n -> distances[index_distance] < sqrt(sum((coord - n[2]) .^ 2)) <= distance
            # Find all neighbors within the current distance but after the last distance
            for (index_neighbor, neighbor) in
                filter(within_distance, collect(enumerate(full_lattice)))
                direction = neighbor - coord
                # Check the direction of the bond, ie, along which axis
                if direction in directions
                    generated_lattice[index_coord, index_neighbor] =
                        (index_distance, findfirst(==(direction), directions))
                else
                    append!(directions, [direction])
                    generated_lattice[index_coord, index_neighbor] =
                        (index_distance, findfirst(==(direction), directions))
                end
            end
        end
    end


    (generated_lattice, findfirst(==(center), full_lattice))

